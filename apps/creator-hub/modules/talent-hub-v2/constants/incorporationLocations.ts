/**
 * Country / state options for the studio onboarding "Country of incorporation"
 * and "State / province of incorporation" dropdowns.
 *
 * MVP scope:
 *  - Country: ISO 3166-1 alpha-2 codes, English short names, alphabetical.
 *    Stored as the alpha-2 code on form state.
 *  - State / province: dropdown for the small set of countries we have a
 *    canonical subdivision list for (US, CA). All other countries fall back to
 *    a free-text input on the form (handled in `StudioFormFields`). When this
 *    list is missing for a country, the form renders a text input instead of a
 *    dropdown.
 *
 * The Figma designs are US-centric (DUNS, etc.), so US states are the priority.
 * Adding more countries to {@link STATE_OPTIONS_BY_COUNTRY} is purely additive.
 */

export type IncorporationCountryOption = {
  /** ISO 3166-1 alpha-2 code, used as the stored form value. */
  code: string;
  name: string;
};

export type IncorporationSubdivisionOption = {
  /** Subdivision code without country prefix (e.g. "CA" for California, "ON" for Ontario). */
  code: string;
  name: string;
};

