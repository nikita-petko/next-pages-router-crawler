export const GetTooltipText = (nameKey: string, args?: { [key: string]: string }): string => {
  if (!args) {
    return TooltipStrings[nameKey] || '';
  }

  let str = TooltipStrings[nameKey];
  Object.keys(args).forEach((key) => {
    str = str.replaceAll(key, args[key]);
  });
  return str;
};
